    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        int selectedrow = tbldriver.getSelectedRow();
        if (selectedrow >= 0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "would you like to delete your information?", "warning", dialogButton);
            if (dialogResult ==JOptionPane.YES_OPTION);{
            VihecleInformation vihecle = (VihecleInformation) tbldriver.getValueAt(selectedrow, 0);
            vihecleinformationdirectory.deletevihecle(vihecle);
            populatetable();
        }
        }
        else
            JOptionPane.showMessageDialog(null, "Please select any account", "warning", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btndeleteActionPerformed

    private void btnsearchnumberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchnumberActionPerformed
        // TODO add your handling code here:
        VihecleInformation result = vihecleinformationdirectory.searchvihecle(txtsearchdriver.getText());
        if(result == null){
            JOptionPane.showMessageDialog(null, "Please type your name on the box", "warning", JOptionPane.WARNING_MESSAGE);
        }
        else
        {
        driverdetailJPanel panel = new driverdetailJPanel(ProcessContainer, result);
        ProcessContainer.add("driverdetailJPanel", panel);
        CardLayout layout = (CardLayout) ProcessContainer.getLayout();
        layout.next(ProcessContainer);
        }
    }//GEN-LAST:event_btnsearchnumberActionPerformed

    private void btnviewdetailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnviewdetailActionPerformed
        // TODO add your handling code here:
        int selectedrow = tbldriver.getSelectedRow();
        if (selectedrow < 0){
            JOptionPane.showMessageDialog(null, "Please select any account!", "warning", JOptionPane.WARNING_MESSAGE);
        }
        else{
            VihecleInformation vihecle = (VihecleInformation) tbldriver.getValueAt(selectedrow, 0);
            driverdetailJPanel panel = new driverdetailJPanel(ProcessContainer, vihecle);
            ProcessContainer.add("driverdetailJPanel", panel);
            CardLayout layout = (CardLayout) ProcessContainer.getLayout();
            layout.next(ProcessContainer);
            
        }
    }//GEN-LAST:event_btnviewdetailActionPerformed

