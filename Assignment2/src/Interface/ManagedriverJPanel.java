    private void populatetable(){
        DefaultTableModel dtm = (DefaultTableModel) tbldriver.getModel();
        dtm.setRowCount(0);
        for (VihecleInformation vihecle : vihecleinformationdirectory.getViheclelist()){
            Object[] row = new Object[2];
            row[0]=vihecle;
            row[1]= vihecle.getModulenum();
            
            dtm.addRow(row);
        }
    }

