    private void populatetable(){

        DefaultTableModel dtm = (DefaultTableModel) tblresult.getModel();
        dtm.setRowCount(0);
        for (VihecleInformation vihecle : vihecleinformationdirectory.getViheclelist()){
           Object[] row = new Object[7];
            row[0] = vihecle;
            row[1] = vihecle.getModulenum();
            row[2]= vihecle.getDateofmanufacturer();
            row[3]= vihecle.getManufactuer();
            row[4] = vihecle.getSeats();
            row[5] = vihecle.getMaximumcapacity();
            row[6] = vihecle.getCity();
            dtm.addRow(row);
        }
        }
    
