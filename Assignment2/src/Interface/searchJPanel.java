    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:
        VihecleInformation result =vihecleinformationdirectory.searchyouruber(txtname.getText(), txtcity.getText(), txtdateofmanufactory.getText(), txtmanufacturer.getText(), txtmaxcapacity.getText(), txtmodulenumber.getText(), txtseatnumber.getText());
        if(result == null){
            JOptionPane.showMessageDialog(null, "No Results", "warning", JOptionPane.WARNING_MESSAGE);
        }
        else{
        DefaultTableModel dtm = (DefaultTableModel) tblresult.getModel();
         TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(dtm);
        tblresult.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(result.getCity().trim()));
        tr.setRowFilter(RowFilter.regexFilter(result.getDateofmanufacturer().trim()));
        tr.setRowFilter(RowFilter.regexFilter(result.getDrivername().trim()));
        tr.setRowFilter(RowFilter.regexFilter(result.getManufactuer()));
        tr.setRowFilter(RowFilter.regexFilter(String.valueOf(result.getSeats()).trim()));
        tr.setRowFilter(RowFilter.regexFilter(String.valueOf(result.getModulenum()).trim().trim()));
        tr.setRowFilter(RowFilter.regexFilter(String.valueOf(result.getMaximumcapacity()).trim()));
        }
    }//GEN-LAST:event_btnsearchActionPerformed

